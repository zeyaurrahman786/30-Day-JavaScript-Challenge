console.log("num17 is not equal to num18 : " + (num17 === num18));



// Activity 4 : Logical Operators

// Task 11 : Write a program that uses the && operator to combine two conditions and log the result to the console.

let num19 = 10;
let num20 = 5;
console.log("Both conditions are true : " + (num19 > 5 && num20 < 10));

// Task 12 : Write a program that uses the || operator to combine two conditions and log the result to the console.

let num21 = 15;
let num22 = 10;
console.log("At least one condition is true : " + (num21 > 5 || num22 < 10));

// Task 13 : Write a program that uses the ! operator to combine two conditions and log the result to the console.

let num23 = 10;
console.log("num23 is not zero : " + !(!num23));



// Activity 5 : Conditional Statements

// Task 14 : Write a program that uses the ternary operator to check if a number is positive or negative and log the result to the console.

